package s04_10;

public class 문제2 {

	public static void main(String[] args) {
		int i=0, j=0;
		
		
		
		
		
		System.out.println("-----------1번-----------");
		for(i=1; i<5; i++) {
			for(j=1; j<5; j++)
				System.out.print(j);
			System.out.println();
		}
		
		
		
		System.out.println("-----------2번-----------");
		for(i=1; i<4; i++) {
			for(j=1; j<5; j++)
				System.out.print(i);
			System.out.println();
		}
		
		
		
		
		System.out.println("-----------3번-----------");
		for(i=1; i<5; i++) {
			for(j=1; j<i+1; j++)
				System.out.print(j);
			System.out.print("\n");
		}
		
		
		
		
		System.out.println("-----------4번-----------");
		for(i=4; i>1; i--) {
			for(j=1; j<i; j++)
				System.out.print("*");
			System.out.println();
		}

	
		System.out.println("-----------5번-----------");
		for(i=3; i>=1; i--) {
			for(j=1; j<=i; j++)
				System.out.print(j);
			System.out.println();
		}
		
		
		
		System.out.println("-----------6번-----------");
		for(i=1; i<=3; i++) {
			for(j=3; j>=i; j--)
				System.out.print(i);
			System.out.println();
		}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
